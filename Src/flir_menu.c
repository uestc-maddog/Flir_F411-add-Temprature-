/*********************************************************************
 * @fn      display_menu
 *
 * @brief   display menu according to current control
 *
 * @param   none
 *
 * @return  
 */
bool display_menu(menuCont_sta Current_Sta)
{
	uint32_t bmp_size;
	uint16_t index;
	uint16_t i;
	uint8_t color;
	uint8_t menuTHstart, menuTHend;
	
	bmp_size = sizeof(menuScreen);
	
	// get ready to start
	LCD_SetCursor(0,0);
	// start writing command
	LCD_WriteRAM_Prepare();	

	// set cousor
	LCD_SetCursor(0,0);
	
	// prepare to write
	LCD_WriteRAM_Prepare();     
  SPILCD_RS_SET;	

	// determine the menu selection color effect area
	// if the menu Sta is empty, goes to the default value
	flir_menuSta = Current_Sta;
	//if(flir_menuSta == empty) flir_menuSta = Brightness;
	
	// define the start threshold
	if((uint8_t)(flir_menuSta) < (MENU_CONTENT - 1))
	{
		// use the enum define to get the const array value
		menuTHstart = menuTH[(uint8_t)(flir_menuSta) + 1];
	}
	else
		menuTHstart = 0;
	
	// use the enum define to get the const array value
	menuTHend = menuTH[(uint8_t)(flir_menuSta)];
	
	
	// block sending
	for(index = 0; index < LCD_CLEAR_ROUND; index ++)
	{
		for(i = 0; i < (SCREEN_SET_BUFSIZE/2); i++)
		{
			if((i < menuTHstart) || (i > menuTHend))
			{
				// get clear buffer ready
				screenSet[2 * i] = (uint8_t)(((menuScreen[(index * (SCREEN_SET_BUFSIZE/2) + i) >> 3] >> (7 - (index * (SCREEN_SET_BUFSIZE/2) + i)%8)) & 0x01)? BLACK:WHITE); 	
				screenSet[2 * i + 1] = screenSet[2 * i];
			}
			else
			{
				//display color according to menu selection
				if((menuScreen[(index * (SCREEN_SET_BUFSIZE/2) + i) >> 3] >> (7 - (index * (SCREEN_SET_BUFSIZE/2) + i)%8)) & 0x01)
				{
					// black color
					screenSet[2 * i] = (uint8_t)(BLACK);
					screenSet[2 * i + 1] = screenSet[2 * i];
				}
				else
				{
					// selected color
					screenSet[2 * i] = (uint8_t)((0xff00 & SELECT_COLOR) >> 8);
					screenSet[2 * i + 1] = (uint8_t)(0x00ff & SELECT_COLOR);
				}
			}
		}
		
		HAL_SPI_Transmit_DMA(&hspi1, (uint8_t*)screenSet, SCREEN_SET_BUFSIZE);
		// polling for 10ms by defaul
		HAL_DMA_PollForTransfer(&LCD_DMA_PORT,HAL_DMA_FULL_TRANSFER,1);		
	}
}	

