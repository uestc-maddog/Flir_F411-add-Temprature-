uint8_t mk;  //baterry flash counter

/*********************************************************************
 * @fn      Addbaterry_menu
 *
 * @brief   Add small baterry icon to screen
 *
 * @param   mode to charging or normal. value if quantity of baterry 
 *
 * @return  
 */
void Addbaterry_menu(Baterrymode mode,Quan_baterry value)
{
	uint8_t i,index;
	
	//batery flash counter add by themself
	mk++;
	//juge the counter 
	if(mk > 29)
		mk = 1;
	if (mode == normal)
	{
		//does the value is equal to the Baterry_full
		if(value==Baterry_full)
			value = Baterry_high;
		if(value == Baterry_empty)   //if the value is equal to the Baterry_empty .The icon is flashing
		{
			if(mk > 15)
			{
				display_normal_baterry(value);	
			}
		}
		else  //display the icon
		{
			display_normal_baterry(value);	
		}
	}
	else if(mode == charging)  //if the mode id equal to charging .the icon is flashing
	{
		if(value == Baterry_full)
		{
			if(mk > 15)
			display_normal_baterry(Baterry_high);
			else
			display_normal_baterry(Baterry_middle);		
		}
		else if(value == Baterry_empty)
		{
			if(mk > 15)
			display_normal_baterry(Baterry_empty);
			else
			display_normal_baterry(Baterry_low);
		}
		else
		{
			if(mk > 15)
			display_normal_baterry(value+1);
			else
			display_normal_baterry(value);
		}
	}
}

