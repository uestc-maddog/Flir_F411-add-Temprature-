		flir_conf.flir_sys_Reticle[0] = (int)((int)((datatemp[3])>>24)-50);   // Reticle XÖáÆ«ÒÆ    ¶ÌÖá
		flir_conf.flir_sys_Reticle[1] = (int)((int)((datatemp[4])>>24)-50);   // Reticle YÖáÆ«ÒÆ    ³¤Öá
		
