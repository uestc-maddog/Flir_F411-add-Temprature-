/*********************************************************************
 * @fn      display_charging
 *
 * @brief   display charging picture
 *
 * @param   value
 *
 * @return  
 */
bool display_charging(Quan_baterry value)
{
	uint32_t bmp_size;
	uint16_t index;
	uint16_t i;
	uint8_t color;
	uint8_t menuTHstart, menuTHend;
	
	bmp_size = sizeof(chargebattery);
	
	// get ready to start
	LCD_SetCursor(0,0);
	// start writing command
	LCD_WriteRAM_Prepare();	

	// set cousor
	LCD_SetCursor(0,0);
	
	// prepare to write
	LCD_WriteRAM_Prepare();     
  SPILCD_RS_SET;	

	
	// define the start threshold
	if((uint8_t)(value) <  (CH_CONTENT))
	{
		// use the enum define to get the const array value
		menuTHstart = chargTH[(uint8_t)(value)]; ;
	}

	
	// use the enum define to get the const array value
	menuTHend = chargTH[0];
	
	
	// block sending
	for(index = 0; index < LCD_CLEAR_ROUND; index ++)
	{
		for(i = 0; i < (SCREEN_SET_BUFSIZE/2); i++)
		{
			if((i < menuTHstart) || (i > menuTHend))
			{
				// get clear buffer ready
				screenSet[2 * i] = (uint8_t)(((chargebattery[(index * (SCREEN_SET_BUFSIZE/2) + i) >> 3] >> (7 - (index * (SCREEN_SET_BUFSIZE/2) + i)%8)) & 0x01)? BLACK:WHITE); 	
				screenSet[2 * i + 1] = screenSet[2 * i];
			}
			else
			{
				//display color according to menu selection
				if(index>40&&index<91)
				{
					// black color
					screenSet[2 * i] = (uint8_t)(BLACK);
					screenSet[2 * i + 1] = screenSet[2 * i];
				}
				else if((chargebattery[(index * (SCREEN_SET_BUFSIZE/2) + i) >> 3] >> (7 - (index * (SCREEN_SET_BUFSIZE/2) + i)%8)) & 0x01)
				{
					// black color
					screenSet[2 * i] = (uint8_t)(BLACK);
					screenSet[2 * i + 1] = screenSet[2 * i];
				}
				else
				{
					// selected color
					screenSet[2 * i] = (uint8_t)(WHITE);
					screenSet[2 * i + 1] = (uint8_t)(WHITE);
				}
			}
		}
		
		HAL_SPI_Transmit_DMA(&hspi1, (uint8_t*)screenSet, SCREEN_SET_BUFSIZE);
		// polling for 10ms by defaul
		HAL_DMA_PollForTransfer(&LCD_DMA_PORT,HAL_DMA_FULL_TRANSFER,1);		
	}
}	

